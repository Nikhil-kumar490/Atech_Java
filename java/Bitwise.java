// Bitwise Opretor----( &, ^ , | , ~ , << , >>)
class Bitwise{
    public static void main(String[] args) {
          int a= 5;//0101
          int b= 6;//0110
        System.out.println(a&b);

        int c= 8;//1000
        int d=9;//1001
        System.out.println(c&d);

        int e= 11;//1011
        int f =4;//0100
        System.out.println(e & f);

        //Leftshift opretor----
        int m = 5;
        int n= m<<2;
        System.out.println(n);

        // Rightshift--
        int k=50;
        int l=k>>2;
        System.out.println(l);

    }
}




