public class Operators {
    public static void main(String[] args) {

      // Relational Opretor----
        int a=5 ;
        int b=6;
        System.out.println(a==b);
        System.out.println(a<b);
        System.out.println(a>b);
        System.out.println(a>=b);
        System.out.println(a<=b);
        System.out.println(a!=b);

// Logical Opretors------

//        boolean c= true;
//        boolean d= false;
//        boolean d= true;
        int c=2;
        int d=3;
        int e=2;
        //&&
        System.out.println(c==d && c==e);

        // \\
        System.out.println(c==d || c==e);


    }

}
